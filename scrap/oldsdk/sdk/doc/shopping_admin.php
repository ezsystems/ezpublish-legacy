<h1>Administrative functions</h1>
<p>
eZ publish has several administrative functions for handling orders. Here you can handle
everything from setting up the shipping and VAT values to search for customers and view statistics.
</p>

<h2>Customer list / view</h2>
<p>
You can view a list of all the customers. A customer is defined as a user who has created
at least one order. When you view the customer you will see the users orders, wish list items
and vouchers.
</p>

<h2>Order list / search</h2>
<p>
The orders are stored in an archive. Here you can search for orders and view specific orders.
You can also change the status of each order so that the customers can track his/hers orders.
</p>

<h2>Statistics</h2>
<p>
The statistics is implemented as a plugin system. Each report is a plugin.
</p>
