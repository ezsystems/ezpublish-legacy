<p>
eZ soap is a library for handling SOAP communication. It has both
server and client functionality.
</p>

<h2>SOAP</h2>
<p>
eZ soap follows the <a href="http://www.w3.org/TR/2001/WD-soap12-part0-20011217/">
SOAP Version 1.2</a> standard. Read the <a href="http://www.w3.org/TR/2001/WD-soap12-part1-20011217/">
Messaging framework specification</a> for details about messaging with SOAP.
</p>



