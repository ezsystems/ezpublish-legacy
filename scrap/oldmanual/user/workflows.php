<h1>Workflows</h1>

<p>
Workflows are a powerful feature of eZ publish which enables you to extend the functionality
of the system. You can extend the publishing function in eZ publish to create an approval path
with one or more editors, or you can extend the shopping checkout function to handle custom
shipping, wrapping, gift certificates or something similar.
</p>

<p>
Visit <a href={"/sdk"|ezurl}>eZ publish SDK</a> and read the tutorials on the subject
to get more information about how to set up and use workflows.
</p>
